        pinsert.setString(5, gender);
        pinsert.setString(6, emptype);
        pinsert.setString(7, hno);
        pinsert.setString(8, country);
        pinsert.setString(9, city);
        pinsert.setString(10, state);
        pinsert.setString(11, dcn.format(dt));
        pinsert.setString(12, user);
        pinsert.setInt(13, deptid);
        pinsert.setString(14, pass);
        //ResultSet rinsert=
        pinsert.executeUpdate();
