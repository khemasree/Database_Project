        PreparedStatement pinsert=null;
        PreparedStatement pselect=null;
        String SQL1="SELECT deptid FROM dbo.Department WHERE dname=?";
        ps = con.prepareStatement(SQL1);
        ps.setString(1, dname);
        ResultSet rs = ps.executeQuery();
       while (rs.next()) {
              System.out.println( rs.getString("deptid"));
              deptid=rs.getInt("deptid");
              System.out.println(deptid);
       }
        String sel="SELECT max(empid) as empid from Employees";
        pselect =con.prepareStatement(sel);
        ResultSet result=pselect.executeQuery();
        while(result.next())
        {
            System.out.println( result.getString("empid"));
            counter=Integer.parseInt(result.getString("empid")) + 1;
            System.out.println(counter);
        }
        String SQL = "INSERT INTO Employees(empid,fname,mname,lname,gender,emptype,Hno,country,city,state,date_of_joining,email,deptid,password) values(?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        pinsert=con.prepareStatement(SQL);
        pinsert.setInt(1,counter);
        pinsert.setString(2, fname);
